<h1>About</h1>
<p>
	This is a SvelteKit example app for DemoTape. It demonstrates that non-React
	frameworks work correctly through DemoTape tunnels.
</p>
<p>
	Navigate between pages using the links above to verify that both server-side
	and client-side routing work as expected.
</p>
<p>
	SvelteKit uses file-based routing and supports server load functions, form
	actions, and API routes -- all of which are exercised in this example.
</p>
